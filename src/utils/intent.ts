/**
 * 极简意图判别：判断一段语音文本是"查询日程"还是"创建/修改日程"。
 *
 * 规则——只看显式的疑问词与典型句式，避免对模型推理结果造成误判。
 * 命中查询模式的典型例子：
 *   "明天有什么事？" / "今天还有几个会" / "周末有空吗" / "下周三 14 点忙吗"
 *   "我下一个会议是几点" / "我和小李约的会议是什么时候"
 *
 * 创建模式（不应被误判为查询）：
 *   "明天下午三点开会一小时"
 *   "提醒我七点起床"
 *   "周三下午 2 点到 4 点和小李讨论方案"
 */

const QUERY_KEYWORDS = [
  '什么事', '有什么', '什么安排', '安排吗', '安排了',
  '几个', '几次',
  '忙吗', '有空', '空闲', '空吗', '有时间',
  '几点', '什么时候', '何时',
  '在做', '在干',
  '冲突', '撞车', '撞了',
  '下一个', '下一场', '下一节', '最近的会议', '最近的安排',
  '查一下', '查询', '看看', '看一下',
];

const QUESTION_TAILS = ['吗', '呢', '嘛'];

/**
 * 是否是查询意图。
 *
 * 触发条件（满足任一）：
 *   - 包含查询关键词
 *   - 以问号结尾（含全角 ？）
 *   - 以"的吗/有吗/几点"等疑问词收尾
 */
export const isQueryIntent = (text: string): boolean => {
  const t = text.trim();
  if (!t) return false;

  // 创建动词前缀直接判否，防止"提醒我看看下周安排"被错判
  const createPrefixes = ['提醒我', '帮我安排', '帮我创建', '帮我加', '加一个', '新建', '创建'];
  if (createPrefixes.some(p => t.startsWith(p))) return false;

  if (/[?？]$/.test(t)) return true;
  if (QUESTION_TAILS.some(tail => t.endsWith(tail))) {
    // "吗/呢/嘛" 结尾且不是命令式 → 视作查询
    return true;
  }
  return QUERY_KEYWORDS.some(k => t.includes(k));
};
